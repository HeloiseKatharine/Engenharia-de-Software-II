//tipos de variáveis 

var num = 5.200;
console.log(num);
//mostra o tipo da variavel 
console.log(typeof (num));//tipo number

var palavra = "abc";
console.log(palavra);
//mostra o tipo da variavel 
console.log(typeof (palavra));//tipo string 


var logica = true; // (true | false)
console.log(logica);
//mostra o tipo da variavel 
console.log(typeof (logica));//tipo boolean 


//quando não é atribuido o valor da variável 
var teste;
console.log(teste);
//mostra o tipo da variavel 
console.log(typeof(teste));// tipo undefined